package HomeWorks.Modul__09_Kollekcii;

/*
Задание 1
 Есть сайт, на котором рассчитывается рейтинг игроков различных сетевых игр.
 Игрок при регистрации указывает ник, а так же список игр, в которые он играет.
 Задача – написать программу, которая:
 регистрирует игроков в системе (должна быть проверка, занят ли ник);
 добавляет рейтинг игроку, в случае его выигрыша в игре;
 выводит список игр, в которые играют все игроки на сайте;
 выводит рейтинг по имени игрока и игре;
 выводит 10 лучших игроков в определенной игре;
 выводит 10 лучших игроков с учетом всех игр.
 */


import java.util.ArrayList;
import java.util.List;

// Методы для вывода топ игроков оставлены для реализации вам, так как требуют более сложной логики сортировки// Класс Игрок
class Player {
    String nickname; // Никнейм игрока
    List<Game> games; // Список игр, в которые играет игрок

    // Конструктор класса Player. Конструктор - это специальный метод, который вызывается при создании нового объекта класса.
    public Player(String nickname) {
        this.nickname = nickname; // 'this' ссылается на текущий объект. Здесь мы присваиваем значение аргумента nickname полю nickname объекта.
        this.games = new ArrayList<>(); // Инициализируем список игр как пустой список.
    }

    // Метод для добавления игры в список игроков
    public void addGame(Game game) {
        if (game != null) { // Проверяем, что игра не null
            games.add(game); // Добавляем игру в список
        } else {
            throw new IllegalArgumentException("Игра не может быть null"); // Если игра null, выбрасываем исключение
        }
    }
}
